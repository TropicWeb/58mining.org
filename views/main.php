      <h3 class=" text-center blockchain"><b>¿Qué es Blockchain?</b></h3>
      <br>
      <br>
      <br>
      <p class="hometex text-center">El <b>Blockchain</b> o cadenas de bloques por su nombre en español, nació en 2008 con la creación del Bitcoin, es el sistema de codifición de la información que sustenta la estructura de la <b>moneda virtual</b> y fue destacada poco después de su surgimiento por la cantidad de aplicaciones que tiene en otras áreas más allá de las transacciones financieras, como la administración pública.
      <br>
      <br>
      <br>
      <b>Blockchain</b> es una tecnología que permite la transferencia de datos digitales codificados de una manera completamente <b>segura</b>. Es vista como el libro de asientos de contabilidad de una empresa en donde se registran todas las entradas y todas las salidas de dinero. La gran innovación es que esta transferencia no requiere de un intermediario centralizado que identifique y certifique la información, sino que la validación de las transacciones está distribuida en múltiples nodos independientes, conocidos popularmente como mineros. Los mineros validan la información a través del protocolo de datos y luego la registran en el “libro de asientos”. <b>Por ejemplo</b>, en una transacción financiera, el minero valida la tenencia de los fondos, la autorización del uso de ellos y la acreditación de los fondos, para luego registrar esa transacción. 
      <br>
      <br>
      <br>
      El Financial Times, realizó un esquema ilustrativo donde se puede apreciar el funcionamiento del blockchain con el envío de dinero entre A y B.</p>
